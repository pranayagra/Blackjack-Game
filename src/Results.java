    /**
     *
     * @param player the player playing the game
     * @param dealer the dealer or house
     * @param count the number of cards in the hand
     * @param total the total hand value
     */
    public Results(BJCard player, BJCard dealer, int count, int total) {
        this.player = player;
        this.dealer = dealer;
        this.counter = count;
        this.total = total;
        initComponents();
        this.dealtotal = this.dealer.computerSumend(this.dealer);
        jLabelCS.setText(jLabelCS.getText() + dealtotal);
        jLabelPS.setText(jLabelPS.getText() + this.total);
        int bet = 0;
        if (counter == 2 && this.total == 21) {
            jLabelWOL.setText("~~~~ BLACKJACK ~~~~");
            bet = (int) (this.player.getBet() * 2.5); //original + 1.5bet
            this.player.setMoney(this.player.getMoney() + bet);
        } else if (this.total > 21 || (this.total < dealtotal && dealtotal <= 21)) {
            bet = -1 * this.player.getBet();
            jLabelWOL.setText("~~~~ YOU LOST ~~~~"); //Original - bet
        } else if (this.total > dealtotal || dealtotal > 21) {
            jLabelWOL.setText("~~~~ YOU WON ~~~~");
            bet = this.player.getBet() * 2; //original + 2bet
            this.player.setMoney(this.player.getMoney() + bet);
        } else if (this.total == dealtotal) {
            jLabelWOL.setText("~~~~ TIE ~~~~"); //Original + bet
            bet = this.player.getBet();
            this.player.setMoney(this.player.getMoney() + bet);
        }
        jLabelMoneyChange.setText(jLabelMoneyChange.getText() + " $" + bet);
        jLabelTotalMoney.setText(jLabelTotalMoney.getText() + " $" + this.player.getMoney());

    }

